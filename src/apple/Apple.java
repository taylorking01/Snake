package apple;

public class Apple {
    private int x; //column of apple
    private int y; //row of apple

    public Apple(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * Getter functions for the apple
     */
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
